export const t = (key, language = 'vi', params = {}) => {
  const keys = key.split('.')
  let value = translations[language]
  
  for (const k of keys) {
    value = value?.[k]
  }
  
  // If no translation found, try fallback to Vietnamese
  if (!value && language !== 'vi') {
    let fallback = translations['vi']
    for (const k of keys) {
      fallback = fallback?.[k]
    }
    value = fallback
  }
  
  if (typeof value === 'string' && params) {
    // Replace placeholders like {total} with actual values
    return value.replace(/\{(\w+)\}/g, (match, paramKey) => {
      return params[paramKey] || match
    })
  }
  
  return value || key
}